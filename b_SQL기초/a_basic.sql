### 관계형 데이터베이스(RDBMS)의 개념 ###

# RDBMS(Related DataBase Management System) : 관계형 데이터베이스느느 데이터를 테이블(표) 형태로 저장하고 테이블 간의 관계를 정의하여 데이터를 관리하는 데이터베이스 시스템 

# RDBMS 특징
# : 테이블(관계)로 데이터를 저장 
# > 각 테이블은 행(레코드)과 열(속성)으로 구성 
# : 스키마를 통해 데이터베이스를 관리 
# > 데이터베이스의 구조와 제약 조건을 정의하는 틀 
# > 테이블 간의 관계와 데이터 타입 등을 포함 
# : SQL 언어를 사용 
# > 데이터를 정의, 조작, 제어하는데 사용하는 언어 
# : 데이터의 정확성과 일관성을 유지하는 여러 무결성 제약 조건을 지원 

# RDBMS 주요 용어 
# 1. 테이블(Table) 
# 2. 열(Column)
# 3. 행(Row)
# 4. 키(Key)